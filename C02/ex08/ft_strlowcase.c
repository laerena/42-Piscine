char	*ft_strlowcase(char *str)
{
	char	*tmp;
	int		i;

	tmp = str;
	i = 0;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] += 32;
		i++;
	}
	return (tmp);
}
